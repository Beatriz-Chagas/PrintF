#include "libftprintf.h"

int	ft_putnbr(int n)
{
	unsigned int	i;

	i = n;
	if (n < 0)
		ft_putchar('-');
	if (i >= 10)
		ft_putnbr(i /10);
	ft_putchar((i % 10) + '0');
}
